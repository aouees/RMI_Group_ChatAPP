    private void SearchFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchFileActionPerformed
     
        if(serverInterface==null){
            showMessageDialog(this,"Connect To Server Please");
        }
        else
        {
            String dir=tfdirectory.getText(),file=tffile.getText(); // get Directory and file 
            if(dir.length()!=0 && file.length()!=0){
                try {
                String  result_search= serverInterface.SearchFile(file,dir);
              
                showMessageDialog(this ,result_search);
                } catch (Exception ex) {
             
            //    Logger.getLogger(ClientForm.class.getName()).log(Level.SEVERE, null, ex);
                    showMessageDialog(this, ex);
            }
            }
        }
        tfdirectory.setText("");
        tffile.setText("");
     
        
                
    }//GEN-LAST:event_SearchFileActionPerformed

