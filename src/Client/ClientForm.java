    private void connect_to_serverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connect_to_serverActionPerformed
        String nameClient=tfnameclient.getText(),idClient=tfidClient.getText();
        if( nameClient.length()!=0 && idClient.length()!=0 && !isConnected){
        Registry registry;
            try { 
                name=nameClient ;
                  id=idClient;
                name_of_clientInterface="clientInterface_"+id;
                clientInterface=new ClientClass(this);
                registry = LocateRegistry.getRegistry("localhost", 5555);
                registry.rebind(name_of_clientInterface, clientInterface);
                  serverInterface =(ServerInterface) registry.lookup("serverInterface");
                  serverInterface.connect_to_server(name, Integer.parseInt(id), name_of_clientInterface);
                  title.setText("Client : "+id+" "+name);
                 isConnected=true;
            } catch (Exception ex) {
              /*
                Logger.getLogger(ClientForm.class.getName()).log(Level.SEVERE, null, ex);*/
                    showMessageDialog(this, ex);
            } 

        }
        else if(isConnected)
            showMessageDialog(this, "You'r Connected before ");
        
        tfnameclient.setText("");
        tfidClient.setText("");

    }//GEN-LAST:event_connect_to_serverActionPerformed
